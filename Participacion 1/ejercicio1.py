# Haz un programa que solicite al usuario dos numeros, y muestre su suma, resta, multiplicacion y division

num1 = int(input("Ingresa el primer numero"))
num2 = int(input("Ingresa el segundo numero"))

suma = (num1 + num2)
resta = (num1 - num2)
multiplicacion = (num1 * num2)
division = (num1 / num2)

print(f'la suma de los numero es {suma}.')
print(f'la resta de los numeros es {resta}.')
print(f'la multiplicacion de los numeros es {multiplicacion}.')
print(f'la division de los numeros es {division}.')